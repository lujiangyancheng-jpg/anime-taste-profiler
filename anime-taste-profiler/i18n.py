def t(lang: str, key: str, tone: str = "standard", **kwargs) -> str:
    if tone == "anime":
        text = ANIME_TEXT.get(lang, {}).get(key)
        if text is None:
            text = ANIME_TEXT.get("en", {}).get(key)
    else:
        text = None

    if text is None:
        text = TEXT.get(lang, TEXT["en"]).get(key, TEXT["en"][key])

    return text.format(**kwargs)
